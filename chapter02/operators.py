a = 3
b = 4
#Arithmetic Operators:
print("the value of 3+4 is:", a+b) #Addition
print("the value of 3-4 is:", a-b) #Subtraction
print("the value of 3*4 is:", a*b) #Multiplication
print("the value of 3/4 is:", a/b) #Division
print("the value of 3%4 is:", a%b) #Modulus
print("the value of 3**4 is:", a**b) #Exponent
print("the value of 3//4 is:", a//b) #Floor Division

#Assignment Operators:
a = 34
b = 34
a += 12
b -= 12
print("the value of a is:", a) # 46
print("the value of b is:", b)  # 22

#Comparison Operators:
a = (4 > 6)
b = (4 < 6)
c = (4 == 6)
d = (4 != 6)
print("the value of 4 > 6 is:", a) # False
print("the value of 4 < 6 is:", b) # True
print("the value of 4 == 6 is:", c) # False
print("the value of 4 != 6 is:", d) # True

#Logical Operators:
bool1 = True
bool2 = False
print("the value of bool1 and bool2 is:", (bool1 and bool2)) # False
print("the value of bool1 or bool2 is:", (bool1 or bool2)) # True
print("the value of not bool2 is:", not bool2) # True
